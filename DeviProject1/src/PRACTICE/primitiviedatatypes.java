package PRACTICE;

public class primitiviedatatypes {
    public static void main(String[] args) {
//primitive data types
        System.out.println("Primitive datatypes--- boolean, numeric");
        System.out.println("Boolean data type -- boolean");
        boolean b1 = true;
        boolean b2 = false;
        System.out.println("boolean b1= " + b1);
        System.out.println("boolean b2= " + b2);
        System.out.println("Numeric data types--- character value, integral value");
        System.out.println("character value is char");
        char c1 = 'a';
        System.out.println("character c1= " + c1);
        System.out.println("integral value is integer and floating point");
        System.out.println(" integer is byte, short, int, long");
        System.out.println("floating point is float, double");
        System.out.println("1. Byte");
        byte b10 = 10;
        System.out.println("byte b10 =" + b10);
        System.out.println("2. Integer");
        int m10; //variable Declaration
        m10 = 10; //variable initialization
        int m11 = 20; // Declare and value initialized
        System.out.println("integer value m10 = " + m10);
        System.out.println("integer m11 is " + m11);
        System.out.println("3. Short");
        short s10 = 30;
        System.out.println("short s10 = " + s10);
        System.out.println("4. Long");
        long l10 = 40;
        System.out.println("long l10 = " + l10);
        System.out.println("5. Float");
        float f10 = 50.5f;
        System.out.println("float f10 = " + f10);
        System.out.println("6. Double");
        double d10 = 60.6;
        System.out.println("double d10= " + d10);
        double num36 = 1233243463.35034;
        System.out.println("check this different output " + num36);
    }
}


